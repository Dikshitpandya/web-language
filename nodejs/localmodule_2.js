var math = {
    add: function (x, y) {
        let temp = x + y;
        return temp;
    },
    sub: function (x, y) {
        let temp = x - y;
        return temp;
    },
    mul: function (x, y) {
        let temp = x * y;
        return temp;
    },
    div: function (x, y) {
        let temp = x / y;
        return temp;
    }
}
module.exports = math;